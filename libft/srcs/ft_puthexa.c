#include "libft.h"

void	ft_puthexa(int nb)
{
	if (nb > 15)
		ft_puthexa(nb / 16);
	nb %= 16;
	if (nb < 10)
		ft_putnbr(nb);
	else
		ft_putchar(nb + 'a' - 10);
}
