#include "libft.h"

void	ft_puthexa(int nb)
{
	int		n;

	if (nb >= 16)
		ft_puthexa(nb / 16);
	n = nb % 16;
	n += (n < 10) ? '0' : 'a' - 10;
	ft_putchar(n);
}
